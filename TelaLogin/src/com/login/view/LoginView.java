    private void btn_entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_entrarActionPerformed
        
        if(txt_emailLogin.getText().matches("") || txt_senhaLogin.getText().matches("")){
            
            JOptionPane.showMessageDialog(rootPane, "Email ou senha incorretos. Tente Novamente.");
            
        }else {
            try {
                loginController login = new loginController();
                login.loginUsuario(this);
            } catch (SQLException ex) {
                Logger.getLogger(LoginView.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(rootPane, "Logado com sucesso.");
            System.exit(0);
        }
    }//GEN-LAST:event_btn_entrarActionPerformed

